
public class RomanToArabic1 extends RomanCalculator {
	public static String romanToArabic1(String roman1)
	{
		String rom1 = null;

		if (roman1.equals("I"))
			rom1="1";
		 if (roman1.equals("II"))
	         rom1="2";
		 if (roman1.equals("III"))
			rom1="3";
		 if (roman1.equals("IV"))
			rom1="4";
		 if (roman1.equals("V"))
			rom1="5";
		 if (roman1.equals("VI"))
			rom1="6";
		 if (roman1.equals("VII"))
			rom1="7";
		 if (roman1.equals("VIII"))
			rom1="8";
		 if (roman1.equals("IX"))
			rom1="9";
		 if (roman1.equals("X"))
			rom1="10";
		
		return rom1;

	}
}
