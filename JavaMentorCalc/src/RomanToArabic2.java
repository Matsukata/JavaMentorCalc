
public class RomanToArabic2 extends RomanCalculator {
	public static String romanToArabic2(String roman2)
	{
		String rom2 = null;

		if (roman2.equals("I"))
			rom2="1";
		 if (roman2.equals("II"))
	         rom2="2";
		 if (roman2.equals("III"))
			rom2="3";
		 if (roman2.equals("IV"))
			rom2="4";
		 if (roman2.equals("V"))
			rom2="5";
		 if (roman2.equals("VI"))
			rom2="6";
		 if (roman2.equals("VII"))
			rom2="7";
		 if (roman2.equals("VIII"))
			rom2="8";
		 if (roman2.equals("IX"))
			rom2="9";
		 if (roman2.equals("X"))
			rom2="10";
		
		return rom2;
	}
}
